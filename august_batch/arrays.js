const fruits = ["apples", "pawpaw", "banana", "pineapple"];
console.log(fruits[1]); //prints the second item
console.log(fruits.length); //prints the length of the array
console.log(fruits.sort().reverse()); //prints a sorted array in a reversed format
fruits.push("orange"); //adds orange to the array
fruits[4] = 'carrot' //changes the third element to carrot
fruits.pop() //removes the last item in the array
fruits.unshift("orange") //adds orange to the beginning of the array
console.log(fruits);
fruits.shift() //removes the first element in the array
console.log(fruits);


